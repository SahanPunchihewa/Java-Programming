/*Write a program to validate a given age of a person using if-else statements.
If age is greater than 18 it should print “Adult” otherwise print “Child”.*/

public class Exercise3 { // Begining of class

    public static void main(String[] args) { // Begining of method

        int age = 10; // Declare Variables

        if (age > 18) { // if Condition

            System.out.println("Adult"); // Display statement

        }

        else { // else

            System.out.println("Child"); // Display statement
        }

    } // End of method

} // End of class
